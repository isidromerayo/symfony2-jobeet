class Job
{
    /**
     * @var integer $id
     */
    private $id;

    /**
     * @var string $type
     */
    private $type;

    /**
     * @var string $company
     */
    private $company;

    /**
     * @var string $logo
     */
    private $logo;

    /**
     * @var string $url
     */
    private $url;

    /**
     * @var string $position
     */
    private $position;

    /**
     * @var string $location
     */
    private $location;

    /**
     * @var string $description
     */
    private $description;

    /**
     * @var string $how_to_apply
     */
    private $how_to_apply;

    /**
     * @var string $token
     */
    private $token;

    /**
     * @var boolean $is_public
     */
    private $is_public;

    /**
     * @var boolean $is_activated
     */
    private $is_activated;

    /**
     * @var string $email
     */
    private $email;

    /**
     * @var \DateTime $expires_at
     */
    private $expires_at;

    /**
     * @var \DateTime $created_at
     */
    private $created_at;

    /**
     * @var \DateTime $updated_at
     */
    private $updated_at;

    /**
     * @var Hcuv\JobeetBundle\Entity\Category
     */
    private $category;


